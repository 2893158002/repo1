Evernote.AlertLogger = {
    debug : function(message) {
        alert("Debug: " + message);
    },

    info : function(message) {
        alert("Info: " + message);
    },

    warn : function(message) {
        alert("Warn: " + message);
    },

    error : function(message) {
        alert("Error: " + message);
    }
};